function sum(a: any, b: any){
    return a + b;
}

console.log(sum(1,1));
//Será a feita a soma dos 2 valores (números) = 2
console.log(sum('1',1));
//Será concatenado a variável 'a'(string), com a variável b (número) = 11
console.log(sum('1','1'));
//Será concatenado as duas variáveis (strings) = 11

